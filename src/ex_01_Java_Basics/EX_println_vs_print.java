package ex_01_Java_Basics;

public class EX_println_vs_print {
    public static void main(String[] args) {
        System.out.println("Hi");
        System.out.println("Bye");
        // println is used for print the words in next line //
        System.out.print("hi");
        System.out.print("bye");
        // print gives the output in the same line //
    }
}
